fun main() {
    // While loop
    // Program to print 10 line

    var i = 1
    while (i <= 10) {
        println("Line $i")
        ++i
    }
    // do while loop
    var index = 7
    do {
        // perfor set of instruction while the value of index is not equal to 0
        println("The value of index is $index")
        --index
    } while (index != 0)

}