fun main() {

    // IfElse

    var animal = "dog"
    if (animal == "cat") {
        println("The animal is not dog")
    } else {
        println("The animal is dog")
    }

    // when  - The is switch staatement of java

    val day = 3
    when (day) {
        1 -> println("Monday")
        2 -> println("Tuesday")
        3 -> println("Wednesday")
        4 -> println("Thursday")
        5 -> println("Friday")
        6 -> println("Saturday")
        7 -> println("Sunday")
        else -> println("Invalid day")
    }
}