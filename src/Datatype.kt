fun main () {
    // DataType and Operator

    var a = 10   // Dont need to tell Datatype
    var b : Int = 5     // If you do
    println(a+b)
    println(a-b)
    println(a*b)
    println(a/b)
    println(a%b)
    println()

    println(a==b)
    println(a>b)
    println(a<b)
    println()

// Pre
    println(++a)
    println(a)
 // Post
    println(--a)
    println(a)
}